// writeCategoryHeader emits the section title bar + subtitle. Both
// run through the banned-literal guard.
func writeCategoryHeader(pdf *fpdf.Fpdf, title, subtitle string) error {
	if err := AssertSection(title+"/title", title); err != nil {
		return err
	}
	if err := AssertSection(title+"/subtitle", subtitle); err != nil {
		return err
	}
	pdf.SetTextColor(20, 20, 22)
	pdf.SetFont("Helvetica", "B", 16)
	pdf.CellFormat(0, 9, title, "", 1, "L", false, 0, "")
	pdf.SetFont("Helvetica", "", 10)
	pdf.SetTextColor(110, 110, 115)
	pdf.MultiCell(0, 5, subtitle, "", "L", false)
	pdf.SetTextColor(0, 0, 0)
	pdf.Ln(2)
	// Accent underline.
	pdf.SetDrawColor(142, 192, 240)
	pdf.SetLineWidth(0.3)
	y := pdf.GetY()
	pageW, _ := pdf.GetPageSize()
	pdf.Line(20, y, pageW-20, y)
	pdf.SetDrawColor(0, 0, 0)
	pdf.Ln(4)
	return nil
}

// sortedMapKeys is a small helper so the cert/sanitizer/audit tables
// have a stable row ordering across builds (Go map iteration is
// non-deterministic; the PDF should be byte-identical given identical
// inputs).
func sortedMapKeys(m map[string]int) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

// adaptFpdf wraps a *fpdf.Fpdf into the templates.CoverRenderer
// interface. The two adapter methods (Ln, PageWidth) compensate for
// fpdf's slightly different shape — Ln takes a float64 but its
// behaviour is identical, and PageWidth() returns the A4 width
// dynamically.
func adaptFpdf(p *fpdf.Fpdf) templates.CoverRenderer {
	return &fpdfAdapter{p: p}
}

type fpdfAdapter struct {
	p *fpdf.Fpdf
}

func (a *fpdfAdapter) AddPage()                                     { a.p.AddPage() }
func (a *fpdfAdapter) SetFont(family, style string, sizePt float64) { a.p.SetFont(family, style, sizePt) }
func (a *fpdfAdapter) SetTextColor(r, g, b int)                     { a.p.SetTextColor(r, g, b) }
func (a *fpdfAdapter) SetDrawColor(r, g, b int)                     { a.p.SetDrawColor(r, g, b) }
func (a *fpdfAdapter) SetLineWidth(w float64)                       { a.p.SetLineWidth(w) }
func (a *fpdfAdapter) SetY(y float64)                               { a.p.SetY(y) }
func (a *fpdfAdapter) SetX(x float64)                               { a.p.SetX(x) }
func (a *fpdfAdapter) GetX() float64                                { return a.p.GetX() }
func (a *fpdfAdapter) GetY() float64                                { return a.p.GetY() }
func (a *fpdfAdapter) Line(x1, y1, x2, y2 float64)                  { a.p.Line(x1, y1, x2, y2) }
func (a *fpdfAdapter) Ln(h float64)                                 { a.p.Ln(h) }
func (a *fpdfAdapter) PageWidth() float64 {
	w, _ := a.p.GetPageSize()
	return w
}
func (a *fpdfAdapter) CellFormat(w, h float64, text, border string, ln int, align string, fill bool, link int, linkStr string) {
	a.p.CellFormat(w, h, text, border, ln, align, fill, link, linkStr)
}
func (a *fpdfAdapter) MultiCell(w, h float64, text, border, align string, fill bool) {
	a.p.MultiCell(w, h, text, border, align, fill)
}

// SanitizeFilename returns a safe Content-Disposition filename for the
// PDF download. Replaces spaces with dashes, strips path separators +
// other suspect characters, lower-cases the customer slug.
func SanitizeFilename(customerName string, from, to time.Time) string {
	slug := strings.ToLower(strings.TrimSpace(customerName))
	var b strings.Builder
	b.Grow(len(slug))
	for _, r := range slug {
		switch {
		case r >= 'a' && r <= 'z', r >= '0' && r <= '9':
			b.WriteRune(r)
		case r == ' ' || r == '-' || r == '_':
			b.WriteRune('-')
		default:
			// Drop everything else.
		}
	}
	customerPart := b.String()
	if customerPart == "" {
		customerPart = "customer"
	}
	return fmt.Sprintf("lucairn-compliance-%s-%s-%s.pdf",
		customerPart,
		from.UTC().Format("20060102"),
		to.UTC().Format("20060102"),
	)
}
